# Define a few example texts to test
test_texts = [
    "I love you so much!",
    "You are a wonderful person.",
    "Go back to your country.",
    "Have a nice day!",
]

# Tokenize and predict
inputs = tokenizer(test_texts, return_tensors="pt", padding=True, truncation=True).to(device)
outputs = model(**inputs)
preds = torch.argmax(outputs.logits, dim=1).cpu().numpy()

# Display predictions
for text, pred in zip(test_texts, preds):
    label = "🟥 HATE / TOXIC" if pred == 1 else "🟩 NOT HATE"
    print(f"Text: {text}\n→ Prediction: {label}\n")